<div class="row text-justify pl-5 pr-5">
    A Balaton a pihenés és a fürdőzés mellett a halairól is nevezetes.
    A Balaton jelen állapotának a megőrzésén nagyon sok önkormányzat, civil szervezet dolgozik.
    Még az 1960-as években több idegenhonos halfaj került be hazánkba, olyanok, amelyek a természet által, spontán módon nem kerülhettek volna a hazai halfaunába. Egyes halfajokat nagyobb mennyiségben a Balatonba telepítettek arra az elképzelésre alapozva, hogy jelentős gazdasági hasznot hoznak.
